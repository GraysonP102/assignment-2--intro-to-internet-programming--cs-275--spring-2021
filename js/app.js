function whiteRice() {
  let x = document.getElementById("whiteRice");
  if (x.style.display === "none") {
    x.style.display = "block";
  } else {
    x.style.display = "none";
  }
}

function CaliRice() {
  let x = document.getElementById("CaliRice");
  if (x.style.display === "none") {
    x.style.display = "block";
  } else {
    x.style.display = "none";
  }
}
